#include <stdio.h>

int main() {
    printf("%i\n", 32768);  /* Prints integer 32768 */
    printf("%.2f\n", 99.999);   /* print 99.999 round off to 2
                                   decimal points. */
    printf("%c\n", (char)227);  /* Prints the symbol of PI.
    For More details, check ASCII Table. */
    printf("%s\n", "Hello World!"); /* Prints the screen "Hello World!"
    on the screen without quotes. */
    return 0;
}
